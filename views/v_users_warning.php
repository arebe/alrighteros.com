<h3>Did you really need to save your credit card information?</h3>

	<div class="rt">
		<img src="/images/creditcards.jpg" width="200" alt="http://upload.wikimedia.org/wikipedia/commons/thumb/4/4f/Credit-cards.jpg/640px-Credit-cards.jpg">
	</div>
You’re making a purchase at awesomecheapstuff.com and the website offers the ability to create an account and save your credit card information for faster check-out later. <br>
<br>

This might seem like a great idea, but perhaps you should consider the trustworthiness of the website you’re entrusting with your data. Would you give your credit card to a store clerk to keep in case you returned to make another purchase?<br>
<br>

<h3>Have you traded the security of your personal data for the convenience of a single log-in?</h3>
	<div class="lt">
		<img src="/images/facebook.jpg" width="200" alt="http://pixabay.com/static/uploads/photo/2013/06/24/09/39/facebook-140903_640.jpg">
	</div>

Increasingly, websites ask you to create a log in for even the most mundane services. Social log-in applications allow web surfers to log into multiple sites using the same credentials as a widely-used social media site, like Facebook.<br>
<br>

However, these same social medias sites have put restrictions on their user base to disallow anonymity.  <a href="http://www.idtheftcenter.org/Cybersecurity/how-do-facebook-hacks-lead-to-identity-theft.html">Identity theft can start with an email address and password to one website which can snowball into access to more critical services and financial accounts</a>.  Banks often require birthday and hometown as security questions - all of which are handily provided by a Facebook profile.<br>
<br>

<h3>Are you using the same password across multiple sites?</h3>

	<div class="rt">
		<img src="/images/heartbleed.png" width="200" alt="http://openclipart.org/image/300px/svg_to_png/192604/Heartbleed-Patch-Needed.png">
	</div>

The recent Heartbleed bug potentially enabled attackers to intercept a “grab bag” of web server data - the contents of which could have included user names and passwords. While the bug did not affect most financial institutions, <a href="http://mashable.com/2014/04/09/heartbleed-bug-websites-affected/">security professionals recommend changing your password</a> on any accounts that might have shared the same password with services that were affected.<br>
<br>

<h3>How do you remember all of your passwords?</h3>

A recent password leak from Australia’s Cupid Media offered <a href="http://krebsonsecurity.com/2013/11/cupid-media-hack-exposed-42m-passwords/">an opportunity to take a look at some of the most common passwords used across the company’s dating services</a>. Over 10% of the site’s 42 million users used one of 10 common passwords; for example, 1.9 million people used the phrase “123456” as their password for this site.<br>
<br>


Strong passwords aren’t always easy to remember but you can protect yourself and your data by using password management software such as <a href="http://keepass.info/">KeePass</a> or <a href="https://agilebits.com/onepassword">1Password</a>.<br>



